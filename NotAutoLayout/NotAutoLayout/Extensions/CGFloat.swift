import Foundation

extension CGFloat {
	
	var halved: CGFloat {
		return self / 2
	}
	
	var doubled: CGFloat {
		return self * 2
	}
	
}

extension CGFloat {
	
	var isNonZero: Bool {
		return self.isNaN == false && self.isZero == false
	}
	
}
